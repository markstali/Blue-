# Esta função calcula media matematica
def calculaMedia(n1,n2,n3,n4):
    media = (n1 + n2 + n3 + n4) / 4
    return media

# Esta função calcula media Mec
def calculaMediaMEC(n1,n2,n3,n4):
    media = (n1 + n2 + n3 + n4) / 3
    print("Media MEC ", media)    